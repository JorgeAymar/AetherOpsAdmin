#!/bin/bash
HOST="194.163.161.196"
USER="sysadmin"
PASS="SysAdmin2025!"

run_remote() {
    CMD="$1"
    expect -c "
    set timeout 60
    spawn ssh -o StrictHostKeyChecking=no $USER@$HOST \"$CMD\"
    expect {
        \"password:\" { send \"$PASS\r\" }
        timeout { exit 1 }
    }
    expect eof
    "
}

echo "--- Check /var/www ---"
run_remote "ls -la /var/www"
